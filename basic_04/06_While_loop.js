//While Loop

let index=0;

while (index<=10) {
    console.log(`value of the index ${index}`);
    index++;
}


let myarray=[1,2,3,4,5];

let arr=0;

while (arr<myarray.length) {
    console.log(`value is ${myarray[arr]}`);
    arr++;
}




//Do While loop

let score=1;

do {
    console.log(`Score is ${score}`);
    score++;
} while (score<10);



let score2=11;

